    private void lbl_checkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_checkMouseClicked
        String username = txt_username.getText();
    if (username.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter a username.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    Connection connection = DatabaseConnection.getConnection();
    if (connection != null) {
        try {
            String query = "SELECT COUNT(*) FROM Users WHERE username = ?";
            java.sql.PreparedStatement statement = connection.prepareStatement(query);
            statement.setString(1, username);
            java.sql.ResultSet resultSet = statement.executeQuery();
            if (resultSet.next()) {
                int count = resultSet.getInt(1);
                if (count > 0) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Username is already taken.", "Unavailable", javax.swing.JOptionPane.WARNING_MESSAGE);
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Username is available.", "Available", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                }
            }
            connection.close();
        } catch (SQLException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error checking username availability.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Failed to connect to the database.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_lbl_checkMouseClicked

  
    
