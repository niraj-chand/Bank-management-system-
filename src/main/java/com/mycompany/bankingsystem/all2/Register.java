    private void Register_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Register_buttonMouseClicked
        String username = txt_username.getText();
    String firstName = jTextField1.getText();
    String lastName = jTextField2.getText();
    String phoneNo = jTextField3.getText();
    String securityQuestion = (String) jComboBox1.getSelectedItem();
    String securityAnswer = jTextField4.getText();
    String password = new String(jPasswordField1.getPassword());

    if (username.isEmpty() || firstName.isEmpty() || lastName.isEmpty() || phoneNo.isEmpty() || securityAnswer.isEmpty() || password.isEmpty()) {
        // Display an error message
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill all fields.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    Connection connection = DatabaseConnection.getConnection();
    if (connection != null) {
        try {
            String query = "INSERT INTO Users (username, password, first_name, last_name, phone_no, security_question, security_answer) VALUES (?, ?, ?, ?, ?, ?, ?)";
            java.sql.PreparedStatement statement = connection.prepareStatement(query);
            statement.setString(1, username);
            statement.setString(2, password);
            statement.setString(3, firstName);
            statement.setString(4, lastName);
            statement.setString(5, phoneNo);
            statement.setString(6, securityQuestion);
            statement.setString(7, securityAnswer);
            int rowsInserted = statement.executeUpdate();
            if (rowsInserted > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Registration successful.", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
            connection.close();
        } catch (SQLException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error during registration.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Failed to connect to the database.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
        new Home().show();
    }//GEN-LAST:event_Register_buttonMouseClicked

